package client.editor.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

import client.editor.FieldEditType;

/**
 * Champ editable d'un objet 
 * <ul>
 * <li></li>
 * <li></li>
 * </ul>
 * 
 * 
 * @author Ludovic Kepka, <b> shionn@gmail.com</b>, 2009-2011
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.FIELD)
public @interface Editable {

	FieldEditType type();

	FieldEditType innerType() default FieldEditType.unknow;

	String applyMethod() default "void";

	float realMaxValue() default 0;
	
	float realMinValue() default 1;
	
	float realStepValue() default 1;

	int intMinValue() default 0;

	int intMaxValue() default 1;

	int intStepValue() default 1;

	String fileFolder() default "./";

	FieldEditType keyType() default FieldEditType.unknow;

	boolean populus() default false;

	float defaultReal() default 0f;

	boolean defaultBool() default false;

}
